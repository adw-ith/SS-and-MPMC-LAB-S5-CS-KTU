ASSUME CS:CODE,DS:DATA
DATA SEGMENT
ARR DB 01H,02H,0AH,05H
LEN DW $-ARR
COUNT DW 0000H
ELE DB 02H
MSG1 DB 0DH,0AH,"ELEMENT FOUND","$"
MSG2 DB 0DH,0AH,"ELEMENT NOT FOUND","$"
DATA ENDS
CODE SEGMENT
START:MOV AX,DATA
      MOV DS,AX
      LEA SI,ARR
      MOV BH,00H
      MOV BL,ELE
  L1:CMP ARR[SI],BL
       JE L2
       INC SI
       LOOP L1
       JMP L3
     L2:INC COUNT
       MOV DX,OFFSET MSG1
       MOV AH,09H
       INT 21H
      L3:MOV AH,4CH
       INT 21H
       CODE ENDS
       END START


